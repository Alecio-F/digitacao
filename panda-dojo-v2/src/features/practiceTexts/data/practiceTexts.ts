export function getPracticeTextById(id: string | null): PracticeText | undefined {
  if (!id) return undefined;
  return PRACTICE_TEXTS.find((text) => text.id === id);
}

export function getNextPracticeText(currentId: string | null): PracticeText {
  const currentIndex = PRACTICE_TEXTS.findIndex((text) => text.id === currentId);
  const nextIndex = currentIndex >= 0 ? (currentIndex + 1) % PRACTICE_TEXTS.length : 0;
  return PRACTICE_TEXTS[nextIndex] ?? PRACTICE_TEXTS[0];
}
